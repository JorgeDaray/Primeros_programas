""" para agregar sangria al texto sigueinte se agrega \t
    y para agregar un salto de linea se puede agregar \n
"""
# por ejemplo
print("python")
print("\tpython")
# nueva linea :
print("lenguajes:\npython\nc++\njavaescript")
# combinado:
print("lenguajes:\n\tpython\n\tc++\n\tjavaescript")
# para encontar y solucionar errores de espacios poner la forma: .strip()
mi_lenguaje_favorito= ' python '
print(mi_lenguaje_favorito.strip())

